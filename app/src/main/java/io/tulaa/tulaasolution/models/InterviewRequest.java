public class InterviewRequest {
	private int pascalsTriangleInput;
	private int[] tripletArrayInput;
	private int[] countTripletsInput;
	private int countTripletInputSum;

	public int getCountTripletInputSum() {
		return countTripletInputSum;
	}

	public void setCountTripletInputSum(int countTripletInputSum) {
		this.countTripletInputSum = countTripletInputSum;
	}

	public int[] getCountTripletsInput() {
		return countTripletsInput;
	}

	public void setCountTripletsInput(int[] countTripletsInput) {
		this.countTripletsInput = countTripletsInput;
	}

	public int[] getTripletArrayInput() {
		return tripletArrayInput;
	}

	public void setTripletArrayInput(int[] tripletArrayInput) {
		this.tripletArrayInput = tripletArrayInput;
	}

	public int getPascalsTriangleInput() {
		return pascalsTriangleInput;
	}

	public void setPascalsTriangleInput(int pascalsTriangleInput) {
		this.pascalsTriangleInput = pascalsTriangleInput;
	}

}
